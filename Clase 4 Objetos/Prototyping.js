/*Prototyping

El patrón de diseño Prototype (Prototipo), tiene como finalidad crear 
nuevos objetos duplicándolos, clonando una instancia creada previamente.

Este patrón especifica la clase de objetos a crear mediante la clonación
 de un prototipo que es una instancia ya creada.
 La clase de los objetos que servirán de prototipo deberá incluir en su interfaz
  la manera de solicitar una copia, 
  que será desarrollada luego por las clases concretas de prototipos.

La programación basada en prototipos es un estilo de programación orientada
 a objetos en la que las clases no están presentes y la reutilización de comportamiento
  (conocido como herencia en lenguajes basados en clases)
   se lleva a cabo a través de un proceso de decoración de objetos existentes
    que sirven de prototipos.Este modelo también se conoce como programación sin clases,
     orientada a prototipos o basada en ejemplos.

El ejemplo original (y más canónico) de un lenguaje basado en prototipos es el 
lenguaje de programación autodesarrollado por David Ungar y Randall Smith.
Sin embargo, el estilo de programación sin clases se ha hecho cada vez más popular 
y ha sido adoptado para lenguajes de programación como JavaScript, 
Cecil, NewtonScript, Io, MOO, REBOL, Kevo,
 Squeak(cuando se utiliza el Viewer framework para manipular los componentes Morphic)
  y varios otros*/